# This program prompts for a temperature in either Fahrenheit or
# Celsius, validates it, and returns the equivalent in the
# opposite unit.

# Prompt user for 
temperature = float(input("Enter temperature: "))
print("1 - Convert Fahrenheit to Celsius")
print("2 - Convert Celisus to Fahrenheit")
choice      = int(input("Enter choice: "))

# Process user choice
if choice == 1:
    degreesC = 5.0 / 9.0 * (temperature - 32.0)
    print (temperature,"degrees F =",degreesC,"degrees C")
elif choice == 2:
    degreesF = 9.0 / 5.0 * temperature + 32.0
    print (temperature,"degrees C =",degreesF,"degrees F")
else:
    print("Invalid selection")
